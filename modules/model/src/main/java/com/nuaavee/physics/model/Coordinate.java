package com.nuaavee.physics.model;

/**
 * @author Adobe Systems Inc
 */
public class Coordinate {

  private final float x;
  private final float y;

  public Coordinate(float x, float y) {
    this.x = x;
    this.y = y;
  }

  public float getX() {
    return x;
  }

  public float getY() {
    return y;
  }
}
